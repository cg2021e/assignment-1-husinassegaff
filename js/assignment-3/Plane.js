function makePlane() {
    let obj = {};

    obj.vertices = [
        1.000000, 0.000000, -1.000000, -0.000000, 0.000000, 1.000000, 18, 113, 39,
        -1.000000, 0.000000, 1.000000, -0.000000, 0.000000, 1.000000, 18, 113, 39,
        -1.000000, 0.000000, -1.000000, -0.000000, 0.000000, 1.000000, 18, 113, 39,
        1.000000, 0.000000, 1.000000, -0.000000, 0.000000, 1.000000, 18, 113, 39,
    ];
 
    obj.indices = [
        0, 1, 2,
        0, 3, 1,
    ];

    obj.vertices = obj.vertices.map((v, i) => {
        if (i % 9 >= 6) {
            return v / 255;
        }
        return v;
    });

    return obj;
}